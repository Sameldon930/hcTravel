<?php

namespace App\Http\Controllers\Admin;

use App\Admin;
use App\Exceptions\ErrorMessageException;
use App\Role;
use App\Services\WebServices\PermissionService;
use Illuminate\Http\Request;
use App\Http\Controllers\Controller;

class AdminController extends Controller
{

    public function index(){

        $search_items = [
            'name' => [
                'type' => 'like',
                'form' => 'text',
                'label' => '姓名',
            ],
            'mobile' => [
                'type' => 'like',
                'form' => 'text',
                'label' => '手机号',
            ],
            'created_at' => [
                'type' => 'date',
            ],
        ];

        $data = Admin::latest()
            ->search($search_items)
            ->paginate()
        ;

        $roles = Role::get();

        return _view('admin.admin.index', compact('data', 'roles'));
    }

    public function edit(Request $request, $id){
        $data = Admin::with('roles')->findOrFail($id);

        $roles = Role::get();

        return view('admin.admin.edit', compact('data', 'roles'));
    }

    public function update(Request $request, $id){
        $admin = Admin::findOrFail($id);

        $this->validate($request, [
            'mobile' => 'required|unique:admins,mobile,' . $admin->id,
            'name' => 'required',
        ]);

        $admin->mobile = $request->get('mobile');
        $admin->name = $request->get('name');
        $admin->save();
        $admin->roles()->sync($request->get('roles'));

        return redirect()->route('admin.admin.index')->with('msg', '修改成功');
    }

    public function store(Request $request){

        $this->validate($request, [
            'password' => 'required|min:6|max:18',
            'mobile' => 'required|unique:admins,mobile',
            'name' => 'required',
        ]);

        $admin = Admin::create([
            'password' => bcrypt($request->get('password')),
            'mobile' => $request->get('mobile'),
            'name' => $request->get('name'),
        ]);
        $admin->roles()->sync($request->get('roles'));

        admin_action_logs($admin, "添加管理员（{$admin->name}）");

        return back()->with('msg', '添加成功');
    }

    public function show($id){
        $data = Admin::findOrFail($id);

        return view('admin.admin.show', compact('data'));
    }

    public function destroy($id){
        $admin = Admin::findOrFail($id);

        if($admin->hasRole('admin')){
            return back()->withErrors('不能删除管理员');
        }

        $admin->roles()->detach();
        $admin->delete();

        admin_action_logs($admin, "删除管理员（{$admin->name}）");

        return back()->with('msg', '删除成功');
    }
}
